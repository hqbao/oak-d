#!/usr/bin/env python3
"""Offline self-test for the Phase-3 stereo calibration math core.

NO device, NO checkerboard photos: we synthesize a ground-truth (GT) stereo rig +
a planar board at diverse poses, project the board's 3D corners into BOTH cameras
with ``cv2.projectPoints`` (using the GT intrinsics/distortion and the GT LEFT->RIGHT
transform), and feed those synthetic image points to the solver -- then assert it
RECOVERS the GT. Because the data is generated by the SAME forward model cv2
inverts, we also add Gaussian pixel noise and re-check under a looser tolerance, so
a pass proves a real fit, not a trivial round-trip of a noiseless model.

Three gates:
  1. Synthetic-board recovery (THE key gate) -- noiseless + noisy. Plus two NEGATIVE
     sub-tests that PROVE the recovery check has teeth: injecting a transposed R
     (R := R.T) and a negated T (T := -T) into a correct solve must each FAIL the
     extrinsic-direction guard, so a real LEFT<->RIGHT swap in solve.py cannot slip
     through the thresholds.
  2. Collector logic -- accepts diverse views, rejects a near-duplicate, rejects a
     one-camera detection failure; an all-FRONTO-PARALLEL sweep reaches n_target
     accepted but is NOT complete (tilt coverage insufficient), while a genuinely
     TILTED + diverse sweep reaches n_target AND completes.
  3. Writer round-trip -- reload via StereoCalib.from_json (proves the cm/m factor)
     and run the shipped calib_check suite on the written file (must PASS).

cv2 appears HERE ONLY as the dev/test forward-model + detection oracle -- never in
the calib package's import path (asserted in test_package_import_is_cv2_free).

Run:  .venv/bin/python -m ui.tests.calib_solve_selftest
"""
