# --------------------------------------------------------------------------- #
# Gate 1b: PROVE the recovery check has teeth -- a corrupted extrinsic must FAIL.
# We take the correctly solved result and inject the two classic silent bugs a
# LEFT<->RIGHT mix-up produces (a TRANSPOSED rotation and a NEGATED translation),
# then assert _assert_recovers now raises. Without this, a passing recovery test
# could still be satisfied by a transpose/flip the thresholds happened to admit.
# --------------------------------------------------------------------------- #
def _solve_clean():
    """Solve the noiseless synthetic rig -- the correct result to then corrupt."""
    views = _project_views(noise_px=0.0)
    return solve_stereo(
        views, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)


def test_extrinsic_transpose_is_caught() -> None:
    """Inject ``R := R.T`` into a correct solve -> the recovery check must FAIL.

    At the 2.5 deg GT rotation, ``||R.T - R|| ~= 0.087`` swamps the tightened
    ``R_err < 5e-3`` gate, so a real LEFT->RIGHT transpose bug in ``solve.py`` can no
    longer slip through. (At the old 0.8 deg GT, ``||R.T - R|| ~= 0.028`` -- below the
    old 5e-2 gate -- which is exactly the hole this sub-test closes.)
    """
    good = _solve_clean()
    # Sanity: the transpose really is geometrically distinct at this GT rotation.
    transpose_gap = float(np.linalg.norm(good.R.T - good.R))
    assert transpose_gap > 5e-3, (
        f"GT rotation too small to distinguish a transpose: "
        f"||R.T-R||={transpose_gap:.4e} <= 5e-3 (raise the GT inter-cam angle)")

    bad = dataclasses.replace(good, R=good.R.T.copy())
    try:
        _assert_recovers(bad, k_tol_frac=0.01, baseline_tol_m=0.001,
                         rms_tol_px=0.05, label="transposed-R(should-fail)")
    except AssertionError as exc:
        assert "extrinsic mismatch" in str(exc), (
            f"transpose was caught, but not by the rotation arm: {exc}")
        print(f"[ok] transpose guard: injecting R:=R.T (||R.T-R||={transpose_gap:.4e}) "
              f"correctly FAILS the recovery check")
        return
    raise AssertionError(
        "recovery check PASSED a transposed extrinsic R -- the rotation guard is "
        "toothless (a LEFT<->RIGHT transpose bug would slip through)")


def test_extrinsic_flip_is_caught() -> None:
    """Inject ``T := -T`` (the right->left inverse) -> the flip-guard must FAIL.

    Complements the transpose test: a pure-translation sign error (the direction-blind
    silent bug the flip-guard was built for) must trip the ``t_err < t_err_if_flipped``
    arm, not pass unnoticed.
    """
    good = _solve_clean()
    bad = dataclasses.replace(good, T=(-good.T).copy())
    try:
        _assert_recovers(bad, k_tol_frac=0.01, baseline_tol_m=0.001,
                         rms_tol_px=0.05, label="negated-T(should-fail)")
    except AssertionError as exc:
        assert "FLIPPED" in str(exc), (
            f"negated T was caught, but not by the flip-guard arm: {exc}")
        print("[ok] flip guard: injecting T:=-T (right->left inverse) correctly "
              "FAILS the recovery check")
        return
    raise AssertionError(
        "recovery check PASSED a negated translation T -- the flip-guard is toothless")


# --------------------------------------------------------------------------- #
# Gate 2: collector logic. We render REAL boards (Phase-1 generator) and warp them
# to synthetic poses so detect_corners runs the genuine cv2 path end-to-end.
# --------------------------------------------------------------------------- #
def _render_board_frame(homography_shift: tuple[float, float],
                        scale: float) -> np.ndarray:
    """Render the Phase-1 board onto a 640x400 canvas at a given offset + scale.

    Returns a uint8 (400,640) frame with the (detectable) board placed inside it.
    ``homography_shift`` moves the board centroid; ``scale`` changes apparent size.
    """
    board = make_checkerboard(PATTERN_COLS, PATTERN_ROWS, square_px=30,
                              margin_squares=1.0)
    bh, bw = board.shape
    # Scale the board.
    new_w = max(1, int(bw * scale))
    new_h = max(1, int(bh * scale))
    board_s = cv2.resize(board, (new_w, new_h), interpolation=cv2.INTER_AREA)

    canvas = np.full((IMAGE_SIZE[1], IMAGE_SIZE[0]), 255, dtype=np.uint8)
    dx, dy = homography_shift
    x0 = int((IMAGE_SIZE[0] - new_w) / 2 + dx)
    y0 = int((IMAGE_SIZE[1] - new_h) / 2 + dy)
    x0 = max(0, min(x0, IMAGE_SIZE[0] - new_w))
    y0 = max(0, min(y0, IMAGE_SIZE[1] - new_h))
    canvas[y0:y0 + new_h, x0:x0 + new_w] = board_s
    return canvas


def _render_tilted_board_frame(tilt_x_deg: float, tilt_y_deg: float,
                               shift: tuple[float, float] = (0.0, 0.0),
                               scale: float = 1.0) -> np.ndarray:
    """Render the board under a perspective TILT so it is NOT fronto-parallel.

    A pure ``_render_board_frame`` is always square-on (skew ~0): perfect for the
    translation/scale diversity axes but useless for exercising the new tilt-coverage
    gate. Here we warp the scaled board with a homography that foreshortens it about
    the X (``tilt_x_deg``) and Y (``tilt_y_deg``) axes, so ``detect_corners`` returns
    a genuinely oblique corner set with elevated skew -- the only way the collector's
    ``skew_ok`` coverage gate can be satisfied.
    """
    board = make_checkerboard(PATTERN_COLS, PATTERN_ROWS, square_px=30,
                              margin_squares=1.0)
    bh, bw = board.shape
    new_w = max(1, int(bw * scale))
    new_h = max(1, int(bh * scale))
    board_s = cv2.resize(board, (new_w, new_h), interpolation=cv2.INTER_AREA)

    # Foreshorten the four corners: tilt about X shrinks the vertical extent of one
    # edge, tilt about Y the horizontal -- a cheap, detectable perspective oblique.
    src = np.float32([[0, 0], [new_w, 0], [new_w, new_h], [0, new_h]])
    tx = np.tan(np.deg2rad(tilt_x_deg))
    ty = np.tan(np.deg2rad(tilt_y_deg))
    dx_warp = new_w * 0.5 * abs(ty)
    dy_warp = new_h * 0.5 * abs(tx)
    dst = np.float32([
        [dx_warp, dy_warp],
        [new_w - dx_warp, 0.0 if tilt_x_deg >= 0 else dy_warp * 2.0],
        [new_w, new_h - dy_warp],
        [dx_warp if tilt_y_deg >= 0 else 0.0, new_h],
    ])
    h_mat = cv2.getPerspectiveTransform(src, dst)
    warped = cv2.warpPerspective(board_s, h_mat, (new_w, new_h), borderValue=255)

    canvas = np.full((IMAGE_SIZE[1], IMAGE_SIZE[0]), 255, dtype=np.uint8)
    sx, sy = shift
    x0 = int((IMAGE_SIZE[0] - new_w) / 2 + sx)
    y0 = int((IMAGE_SIZE[1] - new_h) / 2 + sy)
    x0 = max(0, min(x0, IMAGE_SIZE[0] - new_w))
    y0 = max(0, min(y0, IMAGE_SIZE[1] - new_h))
    canvas[y0:y0 + new_h, x0:x0 + new_w] = warped
    return canvas


def test_collector_logic() -> None:
    """Accepts diverse views, rejects a near-duplicate + a one-camera failure."""
    coll = StereoCheckerboardCollector(
        PATTERN_COLS, PATTERN_ROWS, IMAGE_SIZE)

    # A clearly diverse first view (centred, full size).
    f0 = _render_board_frame((0.0, 0.0), scale=1.0)
    s0 = coll.feed(f0, f0)
    assert s0.found_left and s0.found_right, "view 0 must detect in both"
    assert s0.accepted, f"view 0 should be accepted (reason: {s0.reason})"
    assert coll.accepted_count == 1

    # Near-duplicate of the last accepted view -> REJECTED by the diversity gate.
    s_dup = coll.feed(f0, f0)
    assert not s_dup.accepted, "a near-duplicate must be rejected"
    assert "similar" in s_dup.reason.lower(), s_dup.reason
    assert coll.accepted_count == 1, "duplicate must not grow the accepted set"

    # One-camera detection failure -> REJECTED (right frame is blank white).
    blank = np.full((IMAGE_SIZE[1], IMAGE_SIZE[0]), 255, dtype=np.uint8)
    f_shift = _render_board_frame((120.0, 60.0), scale=1.0)
    s_onecam = coll.feed(f_shift, blank)
    assert not s_onecam.accepted, "one-camera failure must be rejected"
    assert not s_onecam.found_right and s_onecam.found_left, s_onecam.reason
    assert "RIGHT" in s_onecam.reason, s_onecam.reason
    assert coll.accepted_count == 1

    # A genuinely different pose (shifted + smaller) -> ACCEPTED.
    s_diverse = coll.feed(f_shift, f_shift)
    assert s_diverse.accepted, f"diverse view should be accepted: {s_diverse.reason}"
    assert coll.accepted_count == 2
    print(f"[ok] collector: accept diverse, reject duplicate "
          f"(novelty {s_dup.novelty:.3f} < {coll.cfg.min_novelty}), "
          f"reject one-camera failure")


def test_collector_fronto_parallel_count_not_complete() -> None:
    """All-fronto-parallel sweep reaches n_target accepted but is NOT complete.

    This is the Fix-2 guard: a square-on board slid around the frame satisfies the
    per-frame (centroid+size) novelty on TRANSLATION alone, so it can hit the count
    target with skew ~0 -- leaving focal length weakly observable. Completion must be
    withheld (``count_ok`` True, ``skew_ok`` False) and the status must tell the
    operator to tilt the board.
    """
    cfg_target = 6
    coll = StereoCheckerboardCollector(
        PATTERN_COLS, PATTERN_ROWS, IMAGE_SIZE,
        cfg=CollectorConfig(n_target=cfg_target))

    # Distinct (shift, scale) FRONTO-PARALLEL poses: vary translation + depth only.
    poses = [
        ((0.0, 0.0), 1.0),
        ((130.0, 0.0), 1.0),
        ((-130.0, 0.0), 1.0),
        ((0.0, 90.0), 0.85),
        ((0.0, -90.0), 0.85),
        ((110.0, 70.0), 0.7),
        ((-110.0, -70.0), 0.7),
    ]
    last = None
    for shift, scale in poses:
        f = _render_board_frame(shift, scale)
        last = coll.feed(f, f)
        if coll.complete:
            break

    assert last is not None
    assert coll.count_ok, f"fronto sweep should reach the count: {coll.accepted_count}"
    assert coll.accepted_count >= cfg_target
    assert not coll.skew_ok, (
        f"fronto-parallel sweep must NOT satisfy tilt coverage "
        f"(skew_range={last.coverage.skew_range:.4f} >= {coll.cfg.min_skew_spread})")
    assert not coll.complete, "all-fronto-parallel data must NOT report complete"
    assert last.count_ok and not last.skew_ok and not last.complete
    assert "tilt" in last.reason.lower(), last.reason
    cov = last.coverage
    print(f"[ok] collector fronto-parallel: {coll.accepted_count} accepted, "
          f"count_ok={coll.count_ok} but skew_ok={coll.skew_ok} -> NOT complete "
          f"(skew_range={cov.skew_range:.4f} < {coll.cfg.min_skew_spread})")


def test_collector_tilted_reaches_complete() -> None:
    """A genuinely TILTED + diverse sweep reaches n_target AND completes.

    Same count target as the fronto-parallel test, but every view is perspective-
    tilted (high skew) and additionally translated/scaled so each clears the novelty
    gate. This must satisfy BOTH arms -- count and tilt coverage -- and report
    complete, then stop over-collecting.
    """
    cfg_target = 6
    coll = StereoCheckerboardCollector(
        PATTERN_COLS, PATTERN_ROWS, IMAGE_SIZE,
        cfg=CollectorConfig(n_target=cfg_target))

    # (tilt_x_deg, tilt_y_deg, shift, scale): tilt for skew coverage, plus a
    # translation/scale spread so each view is novel on more than one axis.
    specs = [
        (25.0, 0.0, (0.0, 0.0), 1.0),
        (-25.0, 5.0, (120.0, 0.0), 0.95),
        (0.0, 25.0, (-120.0, 0.0), 0.90),
        (20.0, -20.0, (0.0, 80.0), 0.85),
        (-18.0, 18.0, (0.0, -80.0), 0.80),
        (30.0, 10.0, (100.0, 60.0), 0.75),
        (-22.0, -12.0, (-100.0, -60.0), 0.70),
    ]
    last = None
    for tilt_x, tilt_y, shift, scale in specs:
        f = _render_tilted_board_frame(tilt_x, tilt_y, shift, scale)
        last = coll.feed(f, f)
        if coll.complete:
            break

    assert last is not None
    assert coll.count_ok, f"tilted sweep should reach the count: {coll.accepted_count}"
    assert coll.skew_ok, (
        f"tilted sweep must satisfy tilt coverage "
        f"(skew_range={last.coverage.skew_range:.4f} >= {coll.cfg.min_skew_spread})")
    assert coll.complete, f"tilted data must complete: {coll.accepted_count}/{cfg_target}"
    assert coll.accepted_count == cfg_target

    # Once complete, further feeds are ignored (no over-collection).
    f_extra = _render_tilted_board_frame(25.0, 0.0, (0.0, 0.0), 1.0)
    s_after = coll.feed(f_extra, f_extra)
    assert not s_after.accepted and s_after.complete
    assert coll.accepted_count == cfg_target
    cov = s_after.coverage
    print(f"[ok] collector tilted: reached {cfg_target}/{cfg_target}, "
          f"count_ok={coll.count_ok} skew_ok={coll.skew_ok} -> complete "
          f"(coverage cx={cov.centroid_x:.3f} cy={cov.centroid_y:.3f} "
          f"size={cov.size:.3f} skew_range={cov.skew_range:.4f})")


# --------------------------------------------------------------------------- #
# Gate 3: writer round-trip (cm/m factor) + calib_check verdict.
# --------------------------------------------------------------------------- #
def test_writer_roundtrip_and_calib_check() -> None:
    """Write a solved calib, reload via StereoCalib.from_json, prove cm/m + PASS."""
    views = _project_views(noise_px=0.0)
    result = solve_stereo(
        views, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)

    with tempfile.TemporaryDirectory() as tmp:
        path = Path(tmp) / "calib.json"
        written = write_calib_json(result, IMAGE_SIZE, path)
        assert written.exists()

        data = json.loads(written.read_text())
        calib = StereoCalib.from_json(data)

        # cm/m ROUND-TRIP PROOF: the reloaded baseline (loader did *0.01 on the cm
        # we wrote) must equal the solved baseline to 1e-6 m. A missing m->cm
        # multiply in the writer would make this 100x too small and FAIL here.
        assert abs(calib.baseline_m - result.baseline_m) < 1e-6, (
            f"baseline round-trip broken: reloaded {calib.baseline_m * 1000:.6f} mm "
            f"vs solved {result.baseline_m * 1000:.6f} mm "
            f"(cm/m factor bug?)")

        # The raw JSON translation must be in CENTIMETRES (~7.5), not metres.
        T_json = np.asarray(data["T_left_right"], dtype=np.float64).reshape(4, 4)
        json_t_norm = float(np.linalg.norm(T_json[:3, 3]))
        assert abs(json_t_norm - result.baseline_m * 100.0) < 1e-6, (
            f"JSON translation should be cm (~{result.baseline_m * 100:.3f}), "
            f"got {json_t_norm:.3f}")

        # Intrinsics round-trip exactly (fx/fy/cx/cy).
        assert abs(calib.left.fx - result.K_l[0, 0]) < 1e-9
        assert abs(calib.left.cy - result.K_l[1, 2]) < 1e-9
        assert abs(calib.right.fx - result.K_r[0, 0]) < 1e-9

        # END-TO-END TIE-IN: run the shipped calib_check suite on the parsed calib.
        # A good solve must produce NO FAIL rows (WARN is allowed: IMU absent etc.).
        checks = run_checks(calib, reader=None)
        fails = [c for c in checks if c.status == FAIL]
        assert not fails, "calib_check FAILED rows:\n" + "\n".join(
            f"  {c.name}: {c.measured} (exp {c.expected}) -- {c.note}" for c in fails)

    # Also prove calib_to_dict (in-memory) matches the on-disk structure.
    d_mem = calib_to_dict(result, IMAGE_SIZE)
    assert "intrinsics_left" in d_mem and "T_left_right" in d_mem

    print(f"[ok] writer round-trip: solved baseline {result.baseline_m * 1000:.6f} mm "
          f"-> JSON {json_t_norm:.4f} cm -> reloaded {calib.baseline_m * 1000:.6f} mm "
          f"(|err| < 1e-6 m)")
    print(f"[ok] calib_check verdict: PASS ({len(checks)} checks, 0 FAIL)")


