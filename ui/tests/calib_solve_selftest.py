# --------------------------------------------------------------------------- #
# Gate 1c: WIDE-FISHEYE recovery -- the OAK-D W regression. THE gap that let the
# real-device bug through (fx ran away to 1884, stereo RMS diverged to 1e13). The
# existing recovery tests use a MILD ~5 deg-of-distortion pinhole, which the default
# 5-coeff model fits fine, so they never exercise the wide lens. Here we synthesize a
# WIDE rig (fx=fy~=260 => ~101 deg HFOV) with STRONG barrel distortion expressed in the
# 8-coeff RATIONAL model (the model the OAK-D factory calib is a near-superset of),
# project the board through it, and assert solve_stereo RECOVERS fx~=260 (NOT 1884), a
# baseline within a few mm, and a sane stereo RMS -- i.e. the rational model + seeded
# intrinsic guess no longer let the focal length run away on a wide lens.
# --------------------------------------------------------------------------- #
# Wide GT: 640x400, fx=fy~=260 (HFOV ~= 2*atan(320/260) ~= 101 deg -- a real wide lens),
# strong barrel distortion as an 8-coeff rational set (dominant negative k1 + the high-
# order k4..k6 terms the 5-coeff model cannot represent). 7.5 cm baseline, 2.5 deg yaw.
WIDE_K_L = np.array([[260.0, 0.0, 320.0],
                     [0.0, 261.0, 200.0],
                     [0.0, 0.0, 1.0]], dtype=np.float64)
WIDE_K_R = np.array([[259.0, 0.0, 318.0],
                     [0.0, 260.0, 201.0],
                     [0.0, 0.0, 1.0]], dtype=np.float64)
# 8-coeff rational distortion (k1,k2,p1,p2,k3,k4,k5,k6) -- a strong wide barrel.
WIDE_DIST_L = np.array([-0.35, 0.14, 0.0003, -0.0002, -0.03, 0.06, -0.012, 0.005],
                       dtype=np.float64)
WIDE_DIST_R = np.array([-0.34, 0.13, 0.0002, -0.0001, -0.028, 0.055, -0.011, 0.0045],
                       dtype=np.float64)


def _project_wide_views(dist_l: np.ndarray = WIDE_DIST_L,
                        dist_r: np.ndarray = WIDE_DIST_R
                        ) -> list[tuple[np.ndarray, np.ndarray]]:
    """Project the board through the WIDE GT rig into both cameras (per-view L/R).

    Same diverse poses + LEFT->RIGHT chaining as :func:`_project_views`, but with the
    WIDE intrinsics + strong rational distortion. ``dist_l/dist_r`` are overridable so
    the outlier test can corrupt a single view's RIGHT distortion to inject a bad board.
    """
    obj = _object_points()
    views: list[tuple[np.ndarray, np.ndarray]] = []
    for rvec, tvec in _board_poses():
        R_bl, _ = cv2.Rodrigues(rvec)
        t_bl = tvec.reshape(3, 1)
        img_l, _ = cv2.projectPoints(obj, rvec, t_bl, WIDE_K_L, dist_l)
        R_br = GT_R_LR @ R_bl
        t_br = (GT_R_LR @ t_bl).reshape(3) + GT_T_LR
        rvec_br, _ = cv2.Rodrigues(R_br)
        img_r, _ = cv2.projectPoints(
            obj, rvec_br, t_br.reshape(3, 1), WIDE_K_R, dist_r)
        cl = img_l.reshape(-1, 2).astype(np.float32)
        cr = img_r.reshape(-1, 2).astype(np.float32)
        views.append((cl, cr))
    return views


def test_wide_fisheye_recovery() -> None:
    """THE OAK-D W gate: recover fx~=260 (NOT 1884) from a WIDE strongly-distorted rig.

    Asserts the solver returns the wide focal length, a baseline within a few mm of the
    GT, and a sane (non-diverged) stereo RMS -- the exact failure the real device hit.
    Also asserts the recovered fx is FAR from the runaway value the old default model
    produced (~1884), so a regression to the un-seeded 5-coeff model fails this test.
    """
    views = _project_wide_views()
    result = solve_stereo(
        views, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)

    assert result.ok, f"wide solve should converge, got: {result.failure_reason}"
    # fx recovered to the WIDE truth (~260), NOT the runaway ~1884 the bug produced.
    fx_l, fx_r = float(result.K_l[0, 0]), float(result.K_r[0, 0])
    for name, fx, gt in (("L", fx_l, 260.0), ("R", fx_r, 259.0)):
        rel = abs(fx - gt) / gt
        assert rel <= 0.02, (
            f"wide {name} fx={fx:.2f} not within 2% of GT {gt:.1f} (rel {rel*100:.2f}%)")
    # Hard guard on the SPECIFIC failure: fx must be nowhere near the 1884 runaway.
    assert fx_l < 600.0 and fx_r < 600.0, (
        f"focal length ran away (wide-FOV bug): fx_l={fx_l:.1f} fx_r={fx_r:.1f} "
        f"(the regression produced ~1884)")
    # fx/width must sit in the wide-lens band, never the runaway ~2.9.
    assert 0.2 <= fx_l / IMAGE_SIZE[0] <= 2.0, fx_l / IMAGE_SIZE[0]

    # Baseline within a few mm of the GT 75 mm (the bug gave ~961 mm).
    base_mm = result.baseline_m * 1000.0
    assert abs(base_mm - 75.0) <= 5.0, (
        f"wide baseline {base_mm:.2f} mm not within 5 mm of GT 75 mm "
        f"(the bug produced ~961 mm)")
    # Stereo RMS sane (the bug diverged to ~1.5e13 px).
    assert result.stereo_rms < 2.0, (
        f"wide stereo RMS {result.stereo_rms:.3g} px diverged (expected sub-2 px)")

    # The recovered intrinsics must pass the shipped calib_check (the real device hit
    # "L pixel aspect strongly non-square" -- a runaway-fx symptom -- which must be gone).
    calib = StereoCalib.from_json(calib_to_dict(result, IMAGE_SIZE))
    fails = [c for c in run_checks(calib, reader=None) if c.status == FAIL]
    assert not fails, "wide solve FAILED calib_check:\n" + "\n".join(
        f"  {c.name}: {c.measured} -- {c.note}" for c in fails)

    print(f"[ok] WIDE-fisheye recovery: fx_l={fx_l:.3f} fx_r={fx_r:.3f} (GT 260/259, "
          f"NOT 1884), baseline={base_mm:.3f} mm (GT 75), stereo_rms="
          f"{result.stereo_rms:.4f} px, n_views_used={result.n_views_used}, "
          f"flags={result.calibrate_flags}, calib_check 0 FAIL")


# --------------------------------------------------------------------------- #
# Gate 1d: L<->R corner-ORDER reconciliation -- the PRIMARY real-device garbage root
# cause the prior fix MISSED. cv2.findChessboardCorners runs INDEPENDENTLY per image,
# and a cols x rows board has a 180-degree corner-order ambiguity, so a board imaged at
# different in-plane rotations on the two cameras can return the RIGHT corners in the
# fully-REVERSED order relative to the LEFT. Fed straight to stereoCalibrate, the
# mismatched object[k]/left[k] vs right[k] correspondence is "explained" as a
# ~180-degree inter-camera rotation and a runaway ~1 m baseline -- exactly the reported
# `baseline 961 mm`, `R ~ 168 deg`. This gate PROVES the reconcile_lr fix is load-bearing:
# WITHOUT it the solve diverges; WITH it the GT 75 mm baseline is recovered.
# --------------------------------------------------------------------------- #
def test_lr_corner_order_reconciliation() -> None:
    """Reverse the RIGHT corners per view (the real ambiguity); reconcile must cure it.

    Builds the noiseless synthetic stereo dataset, then 180-degree-REVERSES every
    right-camera corner array (``cr[::-1]``) -- the exact form ``findChessboardCorners``
    returns when it locks onto the board the opposite way round on the right camera.

      (a) WITHOUT reconciliation the solve DIVERGES: ``ok=False`` and the baseline blows
          far past the GT 75 mm (toward the reported ~1 m) -- proving the fix is
          load-bearing (mirrors the transpose/flip negative guards).
      (b) WITH ``reconcile_lr`` applied per view, the solver RECOVERS a sane ~75 mm
          baseline, a near-parallel extrinsic, and a low stereo RMS.

    A MIXED dataset (only SOME right views reversed) is also reconciled correctly, since
    ``reconcile_lr`` decides per pair -- the realistic case where the board's in-plane
    angle crosses the flip boundary on only some captures.
    """
    base_views = _project_views(noise_px=0.0)

    # --- (a) ALL right views reversed, NO reconciliation -> must diverge ---
    flipped = [(cl.copy(), cr[::-1].copy()) for (cl, cr) in base_views]
    bad = solve_stereo(
        flipped, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)
    bad_base_mm = bad.baseline_m * 1000.0
    assert not bad.ok, (
        "a 180-degree-reversed RIGHT corner order must NOT report a usable solve "
        f"(got ok=True, baseline={bad_base_mm:.1f} mm) -- reconciliation is the only fix")
    assert bad_base_mm > 200.0, (
        f"without reconciliation the baseline must DIVERGE far past 75 mm "
        f"(toward the reported ~1 m), got {bad_base_mm:.1f} mm -- the flip is not "
        f"actually corrupting the solve, so this test would not prove the fix")

    # --- (b) same data, WITH reconcile_lr per view -> must recover the GT ---
    recon = [reconcile_lr(cl, cr, PATTERN_COLS, PATTERN_ROWS)
             for (cl, cr) in flipped]
    good = solve_stereo(
        recon, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)
    _assert_recovers(good, k_tol_frac=0.01, baseline_tol_m=0.001,
                     rms_tol_px=0.05, label="LR-reconciled(all-flipped)")

    # --- MIXED: reverse only the even-indexed right views, then reconcile -> recover ---
    mixed = [(cl.copy(), (cr[::-1].copy() if i % 2 == 0 else cr.copy()))
             for i, (cl, cr) in enumerate(base_views)]
    mixed_recon = [reconcile_lr(cl, cr, PATTERN_COLS, PATTERN_ROWS)
                   for (cl, cr) in mixed]
    mixed_result = solve_stereo(
        mixed_recon, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)
    assert mixed_result.ok, (
        f"mixed-flip data must recover after per-pair reconciliation: "
        f"{mixed_result.failure_reason}")
    mixed_base_mm = mixed_result.baseline_m * 1000.0
    assert abs(mixed_base_mm - 75.0) <= 1.0, (
        f"mixed-flip reconciled baseline {mixed_base_mm:.3f} mm not within 1 mm of GT 75")

    print(f"[ok] L<->R corner-order reconciliation: WITHOUT it baseline DIVERGED to "
          f"{bad_base_mm:.1f} mm (ok={bad.ok}); WITH reconcile_lr recovered "
          f"{good.baseline_m * 1000:.3f} mm (GT 75, stereo_rms={good.stereo_rms:.4f} px); "
          f"mixed-flip recovered {mixed_base_mm:.3f} mm")


def test_collector_reconciles_flipped_right() -> None:
    """The COLLECTOR reconciles a per-view L<->R flip before it stores the pair.

    The reconciliation must live in the COLLECTION layer so EVERY accepted view's (L,R)
    corners correspond by the time they reach the solve -- not only when a caller invokes
    ``reconcile_lr`` by hand. We monkeypatch the collector's detector so the RIGHT camera
    always returns its corners 180-degree-REVERSED (the ambiguity), feed diverse tilted
    boards until complete, then solve ``collector.views`` directly: a correct collector
    yields a sane ~75 mm baseline; an un-reconciled one would diverge.
    """
    import sky.calib.collector as collector_mod

    coll = StereoCheckerboardCollector(
        PATTERN_COLS, PATTERN_ROWS, IMAGE_SIZE,
        cfg=CollectorConfig(n_target=6))

    real_detect = collector_mod.detect_corners
    call = {"n": 0}

    def detect_flipping_right(gray, cols, rows):
        # Detect normally, but REVERSE every SECOND call (the right camera of each
        # feed()) -- simulating findChessboardCorners returning the opposite ordering on
        # the right image. feed() calls detect_corners L then R, so odd calls are R.
        corners = real_detect(gray, cols, rows)
        call["n"] += 1
        if corners is not None and call["n"] % 2 == 0:
            return corners[::-1].copy()
        return corners

    collector_mod.detect_corners = detect_flipping_right
    try:
        specs = [
            (25.0, 0.0, (0.0, 0.0), 1.0),
            (-25.0, 5.0, (120.0, 0.0), 0.95),
            (0.0, 25.0, (-120.0, 0.0), 0.90),
            (20.0, -20.0, (0.0, 80.0), 0.85),
            (-18.0, 18.0, (0.0, -80.0), 0.80),
            (30.0, 10.0, (100.0, 60.0), 0.75),
            (-22.0, -12.0, (-100.0, -60.0), 0.70),
        ]
        for tilt_x, tilt_y, shift, scale in specs:
            f = _render_tilted_board_frame(tilt_x, tilt_y, shift, scale)
            coll.feed(f, f)
            if coll.complete:
                break
        assert coll.complete, "tilted sweep should complete even with the R flip"
        result = solve_stereo(
            coll.views, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)
    finally:
        collector_mod.detect_corners = real_detect

    # The board here is fronto-rendered (same image to both cameras), so the recovered
    # baseline is ~0 and the extrinsic ~identity; the load-bearing assertion is that the
    # collector did NOT let a 180-flip through to a ~1 m divergence / ~180-degree rotation.
    base_mm = result.baseline_m * 1000.0
    rot_deg = np.degrees(np.arccos(np.clip((np.trace(result.R) - 1) / 2, -1.0, 1.0)))
    assert base_mm < 90.0 and rot_deg < 5.0, (
        f"collector failed to reconcile the R flip: baseline={base_mm:.1f} mm "
        f"rot={rot_deg:.1f} deg (an un-reconciled flip gives ~1 m / ~180 deg)")
    print(f"[ok] collector reconciliation: even with the RIGHT detector 180-reversed, "
          f"collector.views solve to baseline={base_mm:.2f} mm, inter-cam rot="
          f"{rot_deg:.2f} deg (NO ~1 m / ~180-deg divergence)")


def test_outlier_view_rejected() -> None:
    """A single CORRUPTED view is rejected by per-view gating; the solve still recovers.

    One mis-detected board (here: view 5's RIGHT corners NON-RIGIDLY scrambled -- a
    nonlinear per-corner radial stretch + jitter, the way a detector latching onto the
    wrong saddle points warps the grid) cannot be absorbed by a rigid pose, so it
    inflates that view's reprojection RMS. Without gating it would pull the whole fit
    off; we assert (a) the solve still converges, (b) it dropped exactly the bad view
    (``n_views_used < total``), and (c) fx + baseline are still recovered near the GT.
    A pure rigid pixel SHIFT would NOT test the gate -- cv2 just reads it as a board
    translation -- which is why the corruption here is deliberately non-rigid.
    """
    views = _project_wide_views()
    total = len(views)
    bad_idx = 5
    cl, cr = views[bad_idx]
    crd = cr.astype(np.float64)
    # Non-rigid radial scramble about the image centre + small jitter (deterministic).
    centre = np.array([IMAGE_SIZE[0] / 2.0, IMAGE_SIZE[1] / 2.0])
    rel = crd - centre
    stretch = 1.0 + 0.15 * np.sin(np.linalg.norm(rel, axis=1) / 30.0)
    jitter = np.random.default_rng(0).normal(0.0, 3.0, crd.shape)
    cr_bad = (centre + rel * stretch[:, None] + jitter).astype(np.float32)
    views[bad_idx] = (cl, cr_bad)

    result = solve_stereo(
        views, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)

    assert result.ok, f"solve should still converge after gating: {result.failure_reason}"
    assert result.n_views_used < total, (
        f"the corrupted view must be dropped: n_views_used={result.n_views_used} "
        f"== total {total} (outlier NOT rejected)")
    assert result.n_views_used >= total - 2, (
        f"only the bad view(s) should be dropped, kept {result.n_views_used}/{total}")
    # Despite the injected outlier, fx + baseline are still recovered near the GT.
    fx_l = float(result.K_l[0, 0])
    assert abs(fx_l - 260.0) / 260.0 <= 0.03, (
        f"fx not recovered after gating: fx_l={fx_l:.2f} vs GT 260")
    base_mm = result.baseline_m * 1000.0
    assert abs(base_mm - 75.0) <= 8.0, (
        f"baseline not recovered after gating: {base_mm:.2f} mm vs GT 75 mm")
    print(f"[ok] outlier rejection: injected a non-rigidly-scrambled RIGHT view "
          f"({bad_idx}), solver kept {result.n_views_used}/{total} clean views, still "
          f"recovered fx_l={fx_l:.3f} (GT 260) baseline={base_mm:.3f} mm (GT 75)")


def test_too_few_clean_views_fails_honestly() -> None:
    """When the data is mostly junk, the solve FAILS honestly (ok=False), not garbage.

    Corrupt MOST views' RIGHT corners with a large NON-RIGID per-corner scramble (a
    rigid shift would be absorbed as a pose and not flagged; this genuinely destroys
    the grid). The solver must return ``ok=False`` with a clear reason rather than an
    implausible-but-usable calibration -- the wizard's honest-fail path. The failure
    may surface EITHER as "too few clean views ... recapture" (per-view gating dropped
    them) OR as "implausible solve (did not converge)" (the sanity floor caught the
    diverged stereo RMS the surviving junk produced) -- both are honest non-saves.
    """
    views = _project_wide_views()
    # Wreck all but 3 views' RIGHT corners with a HARD non-rigid radial scramble.
    centre = np.array([IMAGE_SIZE[0] / 2.0, IMAGE_SIZE[1] / 2.0])
    rng = np.random.default_rng(99)
    for i in range(len(views) - 3):
        cl, cr = views[i]
        crd = cr.astype(np.float64)
        rel = crd - centre
        stretch = 1.0 + 0.40 * np.sin(np.linalg.norm(rel, axis=1) / 20.0)
        jitter = rng.normal(0.0, 8.0, crd.shape)
        views[i] = (cl, (centre + rel * stretch[:, None] + jitter).astype(np.float32))

    result = solve_stereo(
        views, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)
    assert not result.ok, "a mostly-corrupt dataset must NOT report a usable solve"
    reason = result.failure_reason.lower()
    assert ("recapture" in reason or "did not converge" in reason), (
        f"failure must carry an honest reason, got: {result.failure_reason!r}")
    print(f"[ok] honest fail: {len(views)} views mostly corrupted -> ok=False "
          f"(n_used={result.n_views_used}), reason={result.failure_reason!r}")


def test_min_clean_views_recapture_path() -> None:
    """The 'too few clean views -> recapture' BRANCH specifically (per-view gating).

    A small dataset (9 views) with 2 views whose corners are PURE GARBAGE (random
    in-frame points -- the cleanest model of a totally mis-detected board): the
    per-view gate drops them (and the views they contaminate), leaving FEWER than
    :data:`_MIN_CLEAN_VIEWS`, so the solver returns the explicit "recapture" reason.
    This exercises the gating branch directly (distinct from the sanity-floor branch in
    ``test_too_few_clean_views_fails_honestly``).
    """
    from sky.calib.solve import _MIN_CLEAN_VIEWS
    views = _project_wide_views()[: _MIN_CLEAN_VIEWS + 1]   # 9 views
    rng = np.random.default_rng(5)
    n_corners = PATTERN_COLS * PATTERN_ROWS
    for i in (0, 2):                                        # 2 totally-garbage views
        cl, _ = views[i]
        garbage = np.column_stack([
            rng.uniform(0, IMAGE_SIZE[0], n_corners),
            rng.uniform(0, IMAGE_SIZE[1], n_corners)]).astype(np.float32)
        views[i] = (cl, garbage)

    result = solve_stereo(
        views, PATTERN_COLS, PATTERN_ROWS, SQUARE_SIZE_M, IMAGE_SIZE)
    assert not result.ok, "with < min clean views the solve must fail"
    assert "recapture" in result.failure_reason.lower(), result.failure_reason
    assert result.n_views_used < _MIN_CLEAN_VIEWS, (
        f"recapture branch must report < {_MIN_CLEAN_VIEWS} clean views, "
        f"got {result.n_views_used}")
    print(f"[ok] recapture branch: {len(views)} views, 2 garbage -> "
          f"{result.n_views_used} clean (< {_MIN_CLEAN_VIEWS}) -> ok=False, "
          f"reason={result.failure_reason!r}")


