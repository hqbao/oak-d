# --------------------------------------------------------------------------- #
# Ground truth: OAK-D-like 640x400, fx~=fy~=285, mild distortion, 7.5 cm baseline.
# --------------------------------------------------------------------------- #
PATTERN_COLS, PATTERN_ROWS = 9, 6
SQUARE_SIZE_M = 0.025                  # 25 mm squares
IMAGE_SIZE = (640, 400)               # (width, height)
GT_BASELINE_M = 0.075

GT_K_L = np.array([[285.0, 0.0, 320.0],
                   [0.0, 286.0, 200.0],
                   [0.0, 0.0, 1.0]], dtype=np.float64)
GT_K_R = np.array([[284.0, 0.0, 318.0],
                   [0.0, 285.0, 201.0],
                   [0.0, 0.0, 1.0]], dtype=np.float64)
# Mild radial/tangential distortion (k1,k2,p1,p2,k3) -- realistic for a wide lens.
GT_DIST_L = np.array([-0.045, 0.012, 0.0006, -0.0004, 0.0], dtype=np.float64)
GT_DIST_R = np.array([-0.040, 0.010, 0.0005, -0.0003, 0.0], dtype=np.float64)

# GT LEFT->RIGHT extrinsic: a near-parallel rig translated ~75 mm along -X (the
# right camera sits to the right of the left, so a left-frame point lands at more
# negative X in the right frame), with a deliberate rotation so the test can detect
# a flipped/transposed extrinsic (a pure-translation T is direction-blind). The
# angle is 2.5 deg -- a realistic stereo-rig yaw misalignment, and large enough that
# a TRANSPOSED R is geometrically distinct: ||R.T - R|| ~= 2*sin(2.5 deg) ~= 0.087,
# well above the tightened R_err < 5e-3 recovery threshold, so a LEFT<->RIGHT
# transpose bug in the solver can no longer slip past the flip-guard.
_ang = np.deg2rad(2.5)
GT_R_LR = np.array([[np.cos(_ang), 0.0, np.sin(_ang)],
                    [0.0, 1.0, 0.0],
                    [-np.sin(_ang), 0.0, np.cos(_ang)]], dtype=np.float64)
GT_T_LR = np.array([-GT_BASELINE_M, 0.0, 0.0], dtype=np.float64)  # metres, LEFT->RIGHT


def _object_points() -> np.ndarray:
    """GT board corners in the board frame (Z=0), metres -- detector raster order."""
    obj = np.zeros((PATTERN_ROWS * PATTERN_COLS, 3), dtype=np.float64)
    grid = np.mgrid[0:PATTERN_COLS, 0:PATTERN_ROWS].T.reshape(-1, 2)
    obj[:, :2] = grid
    return obj * SQUARE_SIZE_M


def _board_poses() -> list[tuple[np.ndarray, np.ndarray]]:
    """~15 DIVERSE board poses (rvec, tvec) in the LEFT camera frame.

    Varied distance (0.4-0.9 m), in-frame translation (board centroid swept around
    the image), and tilt (rotation) so the views cover the calibration space.
    """
    poses: list[tuple[np.ndarray, np.ndarray]] = []
    # Centre the board roughly: half the board's metric extent.
    bx = (PATTERN_COLS - 1) * SQUARE_SIZE_M * 0.5
    by = (PATTERN_ROWS - 1) * SQUARE_SIZE_M * 0.5
    # (depth, x_off, y_off, rx_deg, ry_deg, rz_deg) -- 15 diverse poses.
    specs = [
        (0.50, 0.00, 0.00, 0, 0, 0),
        (0.45, -0.06, -0.04, 12, -8, 3),
        (0.55, 0.07, -0.03, -10, 10, -5),
        (0.60, -0.05, 0.05, 8, 14, 6),
        (0.40, 0.04, 0.04, -14, -12, -4),
        (0.65, 0.08, -0.06, 5, -18, 8),
        (0.70, -0.08, 0.06, -6, 16, -10),
        (0.48, 0.03, -0.05, 18, 4, 2),
        (0.58, -0.07, -0.02, -12, -6, 12),
        (0.62, 0.06, 0.05, 9, -14, -7),
        (0.52, -0.04, 0.03, -16, 9, 5),
        (0.44, 0.05, -0.04, 14, 12, -9),
        (0.68, -0.06, -0.05, -8, -16, 4),
        (0.56, 0.07, 0.04, 11, 7, -6),
        (0.50, -0.03, 0.06, -13, -10, 10),
    ]
    for depth, xo, yo, rx, ry, rz in specs:
        # Compose the board orientation from XYZ Euler tilts.
        rot_deg = np.array([rx, ry, rz], dtype=np.float64)
        rvec, _ = cv2.Rodrigues(_euler_xyz_to_R(np.deg2rad(rot_deg)))
        # Place the board so its centre is near (xo, yo, depth) in the left frame.
        tvec = np.array([xo - bx, yo - by, depth], dtype=np.float64).reshape(3, 1)
        poses.append((rvec.reshape(3), tvec.reshape(3)))
    return poses


def _euler_xyz_to_R(rpy: np.ndarray) -> np.ndarray:
    """Rotation from XYZ Euler angles (rad) -- local helper for pose synthesis."""
    rx, ry, rz = rpy
    cx, sx = np.cos(rx), np.sin(rx)
    cy, sy = np.cos(ry), np.sin(ry)
    cz, sz = np.cos(rz), np.sin(rz)
    Rx = np.array([[1, 0, 0], [0, cx, -sx], [0, sx, cx]])
    Ry = np.array([[cy, 0, sy], [0, 1, 0], [-sy, 0, cy]])
    Rz = np.array([[cz, -sz, 0], [sz, cz, 0], [0, 0, 1]])
    return Rz @ Ry @ Rx


def _project_views(noise_px: float = 0.0, seed: int = 0
                   ) -> list[tuple[np.ndarray, np.ndarray]]:
    """Project the GT board at every pose into BOTH cameras -> per-view L/R corners.

    The right-camera projection composes the board->left pose with the GT
    LEFT->RIGHT transform, so the synthetic right points encode the GT extrinsic --
    recovering it back proves the solver's T_left_right direction.
    """
    rng = np.random.default_rng(seed)
    obj = _object_points()
    views: list[tuple[np.ndarray, np.ndarray]] = []
    for rvec, tvec in _board_poses():
        R_bl, _ = cv2.Rodrigues(rvec)            # board -> left
        t_bl = tvec.reshape(3, 1)

        # LEFT image points: project board with the board->left pose.
        img_l, _ = cv2.projectPoints(obj, rvec, t_bl, GT_K_L, GT_DIST_L)

        # RIGHT image points: chain board->left then left->right.
        # X_right = R_LR @ (R_bl @ X_board + t_bl) + t_LR
        R_br = GT_R_LR @ R_bl
        t_br = (GT_R_LR @ t_bl).reshape(3) + GT_T_LR
        rvec_br, _ = cv2.Rodrigues(R_br)
        img_r, _ = cv2.projectPoints(
            obj, rvec_br, t_br.reshape(3, 1), GT_K_R, GT_DIST_R)

        cl = img_l.reshape(-1, 2).astype(np.float64)
        cr = img_r.reshape(-1, 2).astype(np.float64)
        if noise_px > 0.0:
            cl = cl + rng.normal(0.0, noise_px, cl.shape)
            cr = cr + rng.normal(0.0, noise_px, cr.shape)
        views.append((cl.astype(np.float32), cr.astype(np.float32)))
    return views


# --------------------------------------------------------------------------- #
# Gate 1: synthetic-board recovery.
# --------------------------------------------------------------------------- #
def _assert_recovers(result, *, k_tol_frac: float, baseline_tol_m: float,
                     rms_tol_px: float, label: str) -> None:
    """Assert the solved result recovers the GT intrinsics, baseline, direction."""
    # --- intrinsics (fx, fy, cx, cy) within k_tol_frac, both cameras ---
    for name, K_gt, K_got in (("L", GT_K_L, result.K_l), ("R", GT_K_R, result.K_r)):
        for idx, lbl in (((0, 0), "fx"), ((1, 1), "fy"), ((0, 2), "cx"), ((1, 2), "cy")):
            gt = K_gt[idx]
            got = K_got[idx]
            rel = abs(got - gt) / abs(gt)
            assert rel <= k_tol_frac, (
                f"[{label}] {name}.{lbl}: got {got:.3f} vs GT {gt:.3f} "
                f"(rel {rel * 100:.3f}% > {k_tol_frac * 100:.2f}%)")

    # --- baseline magnitude ---
    base = result.baseline_m
    assert abs(base - GT_BASELINE_M) <= baseline_tol_m, (
        f"[{label}] baseline {base * 1000:.3f} mm vs GT {GT_BASELINE_M * 1000:.1f} "
        f"mm (err {abs(base - GT_BASELINE_M) * 1000:.3f} mm > "
        f"{baseline_tol_m * 1000:.3f} mm)")

    # --- EXTRINSIC DIRECTION: recovered T_left_right must match the GT LEFT->RIGHT
    #     transform, NOT its inverse (the silent-bug guard). Compare both R and t. ---
    T_got = result.T_left_right
    R_err = float(np.linalg.norm(T_got[:3, :3] - GT_R_LR))
    t_err = float(np.linalg.norm(T_got[:3, 3] - GT_T_LR))
    # The INVERSE (right->left) would give t ~= -GT_T_LR; assert we are NOT that.
    t_err_if_flipped = float(np.linalg.norm(T_got[:3, 3] - (-GT_T_LR)))
    assert t_err < t_err_if_flipped, (
        f"[{label}] T_left_right looks FLIPPED: ||t-GT||={t_err:.4f} is not "
        f"smaller than ||t-(-GT)||={t_err_if_flipped:.4f}")
    # R_err threshold tightened to 5e-3: the true noiseless recovery is ~7e-8 and the
    # noisy-0.2px recovery is ~2e-3, so 5e-3 holds with margin -- yet it is far below
    # the ||R.T - R|| ~= 0.087 a transposed extrinsic would produce at this 2.5 deg
    # GT rotation, so a LEFT<->RIGHT transpose bug now FAILS this arm (the negative
    # sub-test in test_extrinsic_transpose_is_caught proves the guard has teeth).
    assert R_err < 5e-3 and t_err < baseline_tol_m * 2, (
        f"[{label}] extrinsic mismatch: R_err={R_err:.4e} t_err={t_err * 1000:.3f} mm")

    # --- reprojection RMS: stereo AND per-camera mono fits must be tight ---
    assert result.stereo_rms <= rms_tol_px, (
        f"[{label}] stereo RMS {result.stereo_rms:.4f} px > {rms_tol_px:.3f} px")
    assert result.rms_l <= rms_tol_px, (
        f"[{label}] mono-LEFT RMS {result.rms_l:.4f} px > {rms_tol_px:.3f} px")
    assert result.rms_r <= rms_tol_px, (
        f"[{label}] mono-RIGHT RMS {result.rms_r:.4f} px > {rms_tol_px:.3f} px")

    print(f"[ok] recovery {label}:")
    print(f"       K_l fx/fy/cx/cy = {result.K_l[0,0]:.3f}/{result.K_l[1,1]:.3f}/"
          f"{result.K_l[0,2]:.3f}/{result.K_l[1,2]:.3f}  "
          f"(GT {GT_K_L[0,0]:.1f}/{GT_K_L[1,1]:.1f}/{GT_K_L[0,2]:.1f}/{GT_K_L[1,2]:.1f})")
    print(f"       K_r fx/fy/cx/cy = {result.K_r[0,0]:.3f}/{result.K_r[1,1]:.3f}/"
          f"{result.K_r[0,2]:.3f}/{result.K_r[1,2]:.3f}  "
          f"(GT {GT_K_R[0,0]:.1f}/{GT_K_R[1,1]:.1f}/{GT_K_R[0,2]:.1f}/{GT_K_R[1,2]:.1f})")
    print(f"       baseline = {base * 1000:.4f} mm (GT {GT_BASELINE_M * 1000:.1f} mm, "
          f"err {abs(base - GT_BASELINE_M) * 1000:.4f} mm)")
    print(f"       extrinsic R_err={R_err:.3e}  t_err={t_err * 1000:.4f} mm "
          f"(flip-guard: ||t-(-GT)||={t_err_if_flipped * 1000:.2f} mm)")
    print(f"       RMS  mono L/R = {result.rms_l:.4f}/{result.rms_r:.4f} px  "
          f"stereo = {result.stereo_rms:.4f} px")


